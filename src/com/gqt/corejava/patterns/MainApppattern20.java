package com.gqt.corejava.pattern;

import java.util.Scanner;

public class MainApppattern20 {
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("Enter the size:");
		int n=sc.nextInt();
		
		for(int i=5;i<=n;i--) {
		 
			for(int j=1;j<=i;j++) {
				
			System.out.print(i+ " ");
	}
			System.out.println();
		}
	}
	}





